------------EJECUTA ESO PRIMERO--------
 
 SET SERVEROUTPUT ON;
  --CREACION DE ESPECIFICACION DE PAQUETE
  CREATE OR REPLACE PACKAGE LOS_PRODUCTOS AS
  PROCEDURE CARACTERISTICAS(V_ID PRODUCTO.ID_PRODUCTO%TYPE);
  END LOS_PRODUCTOS;
  
-------------EJECUTA ESO SEGUNDO-----------------
--CREACION DE BODY DE PAQUETE
  CREATE OR REPLACE PACKAGE BODY LOS_PRODUCTOS AS
  PROCEDURE CARACTERISTICAS(V_ID PRODUCTO.ID_PRODUCTO%TYPE) IS
    V_PRODUCTO PRODUCTO.NOMBRE_PRODUCTO%TYPE;
    V_PRECIO PRODUCTO.PRECIO_PRODUCTO%TYPE;
BEGIN
 SELECT NOMBRE_PRODUCTO, PRECIO_PRODUCTO INTO V_PRODUCTO, V_PRECIO
 FROM PRODUCTO WHERE ID_PRODUCTO = V_ID;
 DBMS_OUTPUT.PUT_LINE('ARTICULO ' || V_PRODUCTO);
 DBMS_OUTPUT.PUT_LINE('PRECIO ' || V_PRECIO);
 END CARACTERISTICAS;
END LOS_PRODUCTOS;

-------------LO SIGUIENTE ES PARA LA PRUEBA EL CODIGO LO PODES MODIFICAR DEPENDIENDO DE LO QUE SE DESEA CONSULTAR--
BEGIN
    LOS_PRODUCTOS.CARACTERISTICAS(2);
    END;


