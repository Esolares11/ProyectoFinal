 -----------------------------------------------------------
 FUNCTION F_PRECIO(V_ID INT)
 RETURN NUMBER
 AS
    V_PRECIO NUMBER;
    BEGIN
    SELECT PRECIO_PRODUCTO INTO V_PRECIO
    FROM PRODUCTO WHERE ID_PRODUCTO = V_ID;
    RETURN V_PRECIO;
END F_PRECIO;
 
 
END LOS_PRODUCTOS;