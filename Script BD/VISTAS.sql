--CREAMOS UNA VISTA CLIENTE
CREATE VIEW VISTACLIENTE
AS SELECT PER.NOMBRE1, PER.NOMBRE2, PER.APELLIDO1, CL.FECHA_NACIMIENTO
FROM CLIENTE CL, PERSONA PER
WHERE CL.Id_CLIENTE = PER.Id_persona


--MOSTRAMOS LA VISTA CREADA
SELECT * FROM VISTACLIENTE
--BORRAR UNA VISTA
DROP VIEW VISTACLIENTE


