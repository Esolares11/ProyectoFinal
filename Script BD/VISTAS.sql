CREATE VIEW VISTAEMPLEADO
AS SELECT EM.Id_EMPLEADO, EM.FECHA_CONTRATACION, ST.DESCRIPCION_ESTADO
FROM EMPLEADO EM, ESTADO ST
WHERE EM.Id_ESTADO_EMPLEADO = ST.Id_ESTADO


--MOSTRAMOS LA VISTA CREADA
SELECT * FROM VISTAEMPLEADO
--BORRAR UNA VISTA
DROP VIEW VISTAEMPLEADO